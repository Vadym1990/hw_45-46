function renderModelAsus(select) {

    const asus1 = document.createElement('option');
    const asus2 = document.createElement('option');
    const asus3 = document.createElement('option');

    asus1.innerHTML = 'Asus-1';
    asus2.innerHTML = 'Asus-2';
    asus3.innerHTML = 'Asus-3';

    select.append(asus1, asus2, asus3);
};

